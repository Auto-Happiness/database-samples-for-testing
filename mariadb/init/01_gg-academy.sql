-- gg-academy init: create database, tables, and load CSV data
CREATE DATABASE IF NOT EXISTS `gg_academy` CHARACTER SET = 'utf8mb4' COLLATE = 'utf8mb4_unicode_ci';
USE `gg_academy`;

CREATE TABLE IF NOT EXISTS `students` (
  `student_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `name` VARCHAR(255),
  `grade_level` VARCHAR(50),
  `section` VARCHAR(100),
  `gender` VARCHAR(10),
  `enrollment_status` VARCHAR(50),
  `scholarship` VARCHAR(10)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `teachers` (
  `teacher_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `name` VARCHAR(255),
  `department` VARCHAR(100),
  `teaching_units` INT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `curriculum` (
  `curriculum_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `subject` VARCHAR(100),
  `competency` VARCHAR(255),
  `completion_status` VARCHAR(50)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `attendance` (
  `attendance_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `student_id` VARCHAR(10),
  `date` DATE,
  `status` VARCHAR(50),
  FOREIGN KEY (`student_id`) REFERENCES `students`(`student_id`)
    ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `grades` (
  `grade_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `student_id` VARCHAR(10),
  `subject` VARCHAR(100),
  `grading_period` VARCHAR(20),
  `grade` INT,
  FOREIGN KEY (`student_id`) REFERENCES `students`(`student_id`)
    ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS `finance` (
  `account_id` VARCHAR(10) NOT NULL PRIMARY KEY,
  `student_id` VARCHAR(10),
  `balance` DECIMAL(15,2),
  `overdue_days` INT,
  FOREIGN KEY (`student_id`) REFERENCES `students`(`student_id`)
    ON DELETE SET NULL ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

